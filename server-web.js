// Configura√ß√£o do MongoDB
const MONGODB_URI = process.env.MONGODB_URI || 'mongodb://localhost:27017';
const DB_NAME = 'familia-jamar';
const COLLECTION_NAME = 'contas';

let db = null;
let contas = [];
let nextId = 1;

// Fun√ß√£o para conectar ao MongoDB
async function conectarMongoDB() {
    try {
        console.log('üîÑ Conectando ao MongoDB...');
        const client = new MongoClient(MONGODB_URI);
        await client.connect();
        db = client.db(DB_NAME);
        console.log('‚úÖ Conectado ao MongoDB com sucesso');
        
        // Carregar dados iniciais
        await carregarDados();
    } catch (error) {
        console.log('‚ùå Erro ao conectar ao MongoDB:', error.message);
        console.log('üîç Stack trace:', error.stack);
        
        // Fallback para arquivo local se MongoDB n√£o estiver dispon√≠vel
        console.log('üîÑ Usando fallback para arquivo local...');
        await carregarDadosFallback();
    }
}

// Fun√ß√£o para carregar dados do MongoDB
async function carregarDados() {
    try {
        console.log('üîÑ Carregando dados do MongoDB...');
        
        if (!db) {
            console.log('‚ùå Conex√£o com MongoDB n√£o dispon√≠vel');
            return;
        }
        
        const collection = db.collection(COLLECTION_NAME);
        
        // Buscar todas as contas
        const contasDB = await collection.find({}).toArray();
        contas = contasDB;
        
        // Buscar o pr√≥ximo ID
        const configCollection = db.collection('config');
        const config = await configCollection.findOne({ _id: 'nextId' });
        nextId = config ? config.value : 1;
        
        console.log('‚úÖ Dados carregados do MongoDB:', contas.length, 'contas');
        console.log('üÜî Pr√≥ximo ID:', nextId);
        
        // Log detalhado das contas
        if (contas.length > 0) {
            console.log('üìã Detalhes das contas:');
            contas.forEach((conta, index) => {
                console.log(`  ${index + 1}. ID: ${conta.id}, Descri√ß√£o: ${conta.descricao}, Tipo: ${conta.tipo}, Paga: ${conta.paga}`);
            });
        }
        
    } catch (error) {
        console.log('‚ùå Erro ao carregar dados do MongoDB:', error.message);
        console.log('üîç Stack trace:', error.stack);
        contas = [];
        nextId = 1;
    }
}

// Fun√ß√£o para salvar dados no MongoDB
async function salvarDados() {
    try {
        console.log('üíæ Salvando dados no MongoDB...');
        console.log('üìä Total de contas para salvar:', contas.length);
        console.log('üÜî Pr√≥ximo ID:', nextId);
        
        if (!db) {
            console.log('‚ùå Conex√£o com MongoDB n√£o dispon√≠vel');
            return;
        }
        
        const collection = db.collection(COLLECTION_NAME);
        const configCollection = db.collection('config');
        
        // Limpar cole√ß√£o e inserir todas as contas
        await collection.deleteMany({});
        if (contas.length > 0) {
            await collection.insertMany(contas);
        }
        
        // Atualizar pr√≥ximo ID
        await configCollection.updateOne(
            { _id: 'nextId' },
            { $set: { value: nextId } },
            { upsert: true }
        );
        
        console.log('‚úÖ Dados salvos no MongoDB com sucesso');
        console.log('üìÖ √öltima atualiza√ß√£o:', new Date().toISOString());
        
    } catch (error) {
        console.log('‚ùå Erro ao salvar dados no MongoDB:', error.message);
        console.log('üîç Stack trace:', error.stack);
    }
}

// Fun√ß√£o fallback para carregar dados de arquivo local
async function carregarDadosFallback() {
    try {
        console.log('üîÑ Carregando dados do arquivo local (fallback)...');
        const fs = require('fs-extra');
        const DATA_FILE = path.join(__dirname, 'database', 'contas.json');
        
        // Criar pasta database se n√£o existir
        await fs.ensureDir(path.dirname(DATA_FILE));
        
        // Verificar se o arquivo existe
        if (await fs.pathExists(DATA_FILE)) {
            console.log('üìÑ Arquivo de dados encontrado, lendo...');
            const dados = await fs.readJson(DATA_FILE);
            contas = dados.contas || [];
            nextId = dados.nextId || 1;
            console.log('‚úÖ Dados carregados do arquivo:', contas.length, 'contas');
        } else {
            console.log('üìÅ Arquivo de dados n√£o encontrado, iniciando com dados vazios');
            contas = [];
            nextId = 1;
        }
    } catch (error) {
        console.log('‚ùå Erro ao carregar dados do arquivo:', error.message);
        contas = [];
        nextId = 1;
    }
}

